#include <bits/stdc++.h>

using namespace std;

void solve() {
  int n;
  cin >> n;

  vector<int> a(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }

  if (a[0] != 1)
    cout << "NO\n";
  else
    cout << "YES\n";
  return;
}

int main() {
  int T;
  cin >> T;

  while (T--) {
    solve();
  }

  return 0;
}

